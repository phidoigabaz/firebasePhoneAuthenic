import Foundation
import UIKit

extension UIBarButtonItem {
    static func searchBarItem(target: AnyObject, btnAction: Selector) -> UIBarButtonItem {
        let icon = UIImage(named: "search")
        let btn = UIButton(frame: CGRect(x: 0, y: 0, width: 30, height: 30))
        btn.addTarget(target, action: btnAction, for: .touchUpInside)
        btn.setImage(icon, for: .normal)
        btn.tintColor = AppColor.iconTinColor()
        return UIBarButtonItem(customView: btn)
    }
    
    static func menuBarButtonItem(target: AnyObject, btnAction: Selector) -> UIBarButtonItem {
        let icon = UIImage(named: "iconMenu")?.withRenderingMode(.alwaysTemplate)
        let btn = UIButton(frame: CGRect(x: 0, y: 0, width: 30, height: 30))
        btn.addTarget(target, action: btnAction, for: .touchUpInside)
        btn.setImage(icon, for: .normal)
        btn.tintColor = AppColor.iconTinColor()
        return UIBarButtonItem(customView: btn)
    }
    
    static func notifyBarButtonItem(target: AnyObject, btnAction: Selector) -> MJBadgeBarButton {
        let bellButton = UIButton(type: .custom)
        bellButton.setImage(UIImage(named: "bell"), for: .normal)
        bellButton.frame = CGRect(x: 0, y: 0, width: 50, height: 50)
        bellButton.addTarget(target, action: btnAction, for: .touchUpInside)
        bellButton.tintColor = AppColor.iconTinColor()
        let barButton = MJBadgeBarButton()
        barButton.badgeBGColor = .red
        barButton.badgeTextColor = .black
        barButton.badgeOriginX = 10
        barButton.badgeMinSize = 14.0
        barButton.badgeSize = 25.0
        barButton.tintColor = AppColor.iconTinColor()
        barButton.setup(customButton: bellButton)
        return barButton
    }
    
    static func backBarButtonItem(target: AnyObject, btnAction: Selector) -> UIBarButtonItem {
        let icon = UIImage(named: "left-arrow")?.withRenderingMode(.alwaysTemplate)
        let btn = UIButton(frame: CGRect(x: 0, y: 0, width: 30, height: 30))
        btn.addTarget(target, action: btnAction, for: .touchUpInside)
        btn.setImage(icon, for: .normal)
        btn.tintColor = AppColor.iconTinColor()
        return UIBarButtonItem(customView: btn)
    }
    
    static func backBarButtonItem1(target: AnyObject, btnAction: Selector) -> UIBarButtonItem {
        let icon = UIImage(named: "back")?.withRenderingMode(.alwaysTemplate)
        let btn = UIButton(frame: CGRect(x: 0, y: 0, width: 30, height: 30))
        btn.addTarget(target, action: btnAction, for: .touchUpInside)
        btn.setImage(icon, for: .normal)
        return UIBarButtonItem(customView: btn)
    }
}

        
        
        
        
        

        

 
        
        
        
        
        
        
    
        

        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        


        
